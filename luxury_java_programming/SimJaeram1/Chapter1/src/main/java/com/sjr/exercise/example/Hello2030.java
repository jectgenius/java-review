package com.sjr.exercise.example;               // com.sjr.exercise.example 패키지 선언문

public class Hello2030 {                        // Hello2030 클래스 졍의 시작
    public static void main(String[] args) {    // main() 메소드 정의 시작
        // 변수 n 선언
        int n = 2030;                           // int형 변수 n 선언 동시에 2030 대입하여 초기화
        // 문자열 출력
        System.out.println("헬로" + n);          // System.out.println() 메소드 호출하여 문자열 "헬로 2030" 출력
    }                                           // main() 메소드 정의 시작
}                                               // Hello2030 클래스 졍의 끝
